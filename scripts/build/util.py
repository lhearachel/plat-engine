def signed_int(i: int) -> int:
    if i > 127:
        return (256 - i) * -1
    return i


def sint_to_byte(i: int) -> bytes:
    sint = i
    if i < 0:
        sint = 256 + i
    
    return sint.to_bytes(1, 'little')


def snake_case(s: str) -> str:
    return '_'.join(
        re.sub('([A-Z][a-z]+)',
               r' \1',
               re.sub('([A-Z]+)',
                      r' \1',
                      s.replace('-', ' ')
                )
        ).split()
    ).lower()


