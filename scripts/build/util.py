def signed_int(i: int) -> int:
    if i > 127:
        return (256 - i) * -1
    return i


def sint_to_byte(i: int) -> bytes:
    sint = i
    if i < 0:
        sint = 256 + i
    
    return sint.to_bytes(1, 'little')


